-- ================================
-- 테이블 DDL
-- ================================

CREATE TABLE students (
    id INT PRIMARY KEY,
    name VARCHAR(50),
    grade INT,        -- 학년 (1~3)
    class VARCHAR(10) -- 반 (A, B, C)
);

CREATE TABLE subjects (
    id INT PRIMARY KEY,
    name VARCHAR(50),
    teacher VARCHAR(50),
    credit INT  -- 학점
);

CREATE TABLE scores (
    id INT PRIMARY KEY,
    student_id INT,
    subject_id INT,
    score INT,        -- 점수 (0~100)
    exam_date DATE,
    FOREIGN KEY (student_id) REFERENCES students(id),
    FOREIGN KEY (subject_id) REFERENCES subjects(id)
);

CREATE TABLE attendance (
    id INT PRIMARY KEY,
    student_id INT,
    attend_date DATE,
    status VARCHAR(10), -- 'present', 'absent', 'late'
    FOREIGN KEY (student_id) REFERENCES students(id)
);

